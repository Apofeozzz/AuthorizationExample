import UIKit

extension UIFont {
	
	static func futuraBoldWithSize(_ size: CGFloat) -> UIFont {
		
		UIFont(name: "Futura-Bold", size: size)!
		
	}
	
	static func futuraMediumWithSize(_ size: CGFloat) -> UIFont {
		
		UIFont(name: "Futura-Medium", size: size)!
		
	}
	
}
